        <p>Obama’s personal charisma, stirring oratory, and his campaign promise to bring change to the established political system resonated with many Democrats, especially young and minority voters. On January 3, 2008, Obama won a surprise victory in the first major nominating contest, the Iowa caucus, over Sen. Hillary Clinton, who was the overwhelming favourite to win the nomination. Five days later, however, Obama finished second to Clinton in the New Hampshire primary, and a bruising—and sometimes bitter—primary race ensued. Obama won more than a dozen states—including Illinois, his home state, and Missouri, a traditional political bellwether—on Super Tuesday, February 5. No clear front-runner for the nomination emerged, however, as Clinton won many states with large populations, such as California and New York. Obama produced an impressive string of victories later in the month, handily winning the 11 primaries and caucuses that immediately followed Super Tuesday, which gave him a significant lead in pledged delegates. His momentum slowed in early March when Clinton won significant victories in Ohio and Texas. Though still maintaining his edge in delegates, Obama lost the key Pennsylvania primary on April 22. Two weeks later he lost a close contest in Indiana but won the North Carolina primary by a large margin, widening his delegate lead over Clinton. She initially had a big lead in so-called superdelegates (Democratic Party officials allocated votes at the convention that were unaffiliated with state primary results), but, with Obama winning more states and actual delegates, many peeled away from her and went to Obama. On June 3, following the final primaries in Montana and South Dakota, the number of delegates pledged to Obama surpassed the total necessary to claim the Democratic nomination.</p>
       
    
